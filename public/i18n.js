let currentLang = localStorage.getItem('subarnapasal.lang') || 'en';

function t(key) {
  return I18N[currentLang]?.[key] ?? I18N.en[key] ?? key;
}

function setLanguage(lang) {
  currentLang = lang === 'ne' ? 'ne' : 'en';
  localStorage.setItem('subarnapasal.lang', currentLang);
  document.documentElement.lang = currentLang;
  document.title = t('pageTitle');
}

function categoryLabel(cat) {
  const map = {
    ring: 'catRing',
    necklace: 'catNecklace',
    bangle: 'catBangle',
    earring: 'catEarring',
    coin: 'catCoin',
    bar: 'catBar',
    other: 'catOther'
  };
  return t(map[cat] || 'catOther');
}

function applyStaticI18n() {
  document.querySelectorAll('[data-i18n]:not([data-currency-field])').forEach((el) => {
    const key = el.dataset.i18n;
    if (key) el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-option]').forEach((el) => {
    el.textContent = t(el.dataset.i18nOption);
  });
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const text = t(el.dataset.i18nTitle);
    el.title = text;
    el.setAttribute('aria-label', text);
  });
  const langSelect = document.getElementById('language-select');
  if (langSelect) langSelect.value = currentLang;
}
